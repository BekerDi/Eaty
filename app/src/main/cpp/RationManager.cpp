#include "RationManager.h"

std::string RationManager::getRationTitle(const std::string& rationId) {
    if (rationId == "high_protein") {
        return u8"Высокобелковая диета";
    } else if (rationId == "calorie_deficit") {
        return u8"Диета с дефицитом калорий";
    } else {
        return u8"Неизвестный рацион";
    }
}

std::string RationManager::getRationShortDescription(const std::string& rationId) {
    if (rationId == "high_protein") {
        return u8"Рацион для наращивания мышечной массы с высоким содержанием белка.";
    } else if (rationId == "calorie_deficit") {
        return u8"Рацион для похудения за счёт дефицита калорий.";
    } else {
        return u8"Описание недоступно.";
    }
}

