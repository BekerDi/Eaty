package com.example.myeaty;

public class UserProfile {
    public String name;
    public int gender;
    public int age;
    public int weight;
    public int height;
    public int goal;
    public int activityLevel;

    public UserProfile(String name, int gender, int age, int weight, int height, int goal, int activityLevel) {
        this.name = name;
        this.gender = gender;
        this.age = age;
        this.weight = weight;
        this.height = height;
        this.goal = goal;
        this.activityLevel = activityLevel;
    }
}

